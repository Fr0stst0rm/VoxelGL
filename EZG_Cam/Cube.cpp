#include <GL/glew.h>
#include "Cube.h"

Cube::Cube()
{
	//Reset all render sides to true
	memset(&m_settings, 0xff, sizeof(m_settings));
	m_settings.glow = false;
	m_settings.transparent = false;
}

Cube::~Cube()
{
}

void Cube::createMesh() {
	createCubeMesh();
}

